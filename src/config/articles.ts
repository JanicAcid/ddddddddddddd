/** Получить все статьи (для списка и sitemap) */
export function getAllArticles(): Article[] {
  return ALL_ARTICLES.sort((a, b) => new Date(b.datePublished).getTime() - new Date(a.datePublished).getTime())
}

/** Получить статью по slug (для отдельной страницы) */
export function getArticleBySlug(slug: string): Article | undefined {
  return ALL_ARTICLES.find(a => a.slug === slug)
}

/** Получить все слаги (для generateStaticParams) */
export function getAllArticleSlugs(): string[] {
  return ALL_ARTICLES.map(a => a.slug)
}

/** Получить уникальные категории */
export function getAllCategories(): { name: string; slug: string }[] {
  const map = new Map<string, string>()
  ALL_ARTICLES.forEach(a => { if (!map.has(a.categorySlug)) map.set(a.categorySlug, a.category) })
  return Array.from(map.entries()).map(([slug, name]) => ({ name, slug }))
}

/** Получить связанные статьи */
export function getRelatedArticles(currentSlug: string, limit = 3): Article[] {
  const current = getArticleBySlug(currentSlug)
  if (!current) return []
  const related = (current.relatedSlugs || [])
    .map(s => getArticleBySlug(s))
    .filter((a): a is Article => !!a)
  if (related.length >= limit) return related.slice(0, limit)
  // Дополняем статьями из той же категории
  const sameCategory = ALL_ARTICLES.filter(a => a.slug !== currentSlug && a.categorySlug === current.categorySlug)
  return [...related, ...sameCategory].slice(0, limit)
}
